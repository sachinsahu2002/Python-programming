# While loop in Python
i = 0
while (i<=3):
    print(i)
    i = i+1

print("Done with loop")

# incremental while loop
i = int(input("Enter the number: "))
print(i)
while (i<38):
    i = int(input("Enter the number: "))
    print(i)
print("Done with loop")

# Decremental while loop
count = 5
while (count > 0):
    print(count)
    count = count-1
else:
    print("I am inside else")

